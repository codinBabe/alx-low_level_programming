#include "cal.h"

/**
 * add - a function that add 2 numbers
 * @a: first num
 * @b: second num
 * Return: result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - a function that add 2 numbers
 * @a: first num
 * @b: second num
 * Return: result
 */

int sub(int a, int b)
{
        return (a - b);
}

/**
 * mul - a function that mul 2 numbers
 * @a: first num
 * @b: second num
 * Return: result
 */

int mul(int a, int b)
{
        return (a * b);
}

/**
 * div - a function that div 2 numbers
 * @a: first num
 * @b: second num
 * Return: result
 */

int div(int a, int b)
{
        return (a / b);
}

/**
 * mod - a function that div 2 numbers
 * @a: first num
 * @b: second num
 * Return: result
 */

int mod(int a, int b)
{
        return (a % b);
}
