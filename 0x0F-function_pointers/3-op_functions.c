#include "3-calc.h"
/**
 * op_add - add 2 numbers
 * @a:first number
 * @b:second number
 * Return:sum of two numbers
 */
int op_add(int a, int b)
{
	return (a + b);

}
/**
 * op_sub - subtract 2 numbers
 * @a:first number
 * @b:second number
 * Return:subtraction of two number
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mutiple of two numbers
 * @a:first number
 * @b:second number
 * Return:multiplication of two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two numbers
 * @a:first number
 * @b:second number
 * Return:divided number
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - check remainder when b divide a
 * @a:first number
 * @b:second number
 * Return:modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}

